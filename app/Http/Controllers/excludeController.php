     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request,$city_id)
    {
        $exclude = new exclude;
        $exclude->start_day = $request->input('start_day');
        $exclude->end_day = $request->input('end_day');
        $exclude->city_id = $city_id;
        $delivery_id = implode(',',$request->input('delivery_id'));
        $exclude->delivery_id = $delivery_id;
        if($exclude->save())
        {
            return new excludeResource($exclude);
        }

    }

