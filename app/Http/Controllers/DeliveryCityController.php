     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request,$city_id)
    {
        
        $deliverycities = [];
        foreach($request->input('delivery_times') as $delivery)
        {
            $deliverycity = new deliverycity();
            $deliverycity->delivery_id = $delivery;
            $deliverycity->city_id = $city_id;
            $deliverycities[] = $deliverycity;
            $deliverycity->save();
        }
        return new delivery_city($deliverycities);
    }

